// top — AXC3000 board top level for the HyperBus bandwidth test (Agilex-3 A3CY100BM16AE7S).
//
// Data path:
//   bw_sys (Qsys)                              fpga/axc3000/qsys/
//     * IOPLL: clk (50 MHz, 0deg = HyperBus CK word clock) + clk2x (100 MHz, 0deg = SDR byte clock),
//       both from the 25 MHz board XO. (The Qsys export is still named "clk90"; for the SDR PHY it
//       now carries the 100 MHz 2x byte clock — NOT a 90deg phase. See make_bw_sys.tcl.)
//     * reset controller -> synchronous active-high fabric reset (clk / 50 MHz domain)
//     * JTAG-to-Avalon-MM master bridge  --- exported Avalon-MM master (byte addressed)
//         |
//         v  (byte->word CSR adapter below)
//   hyperram_bw_test (traffic gen + scoreboard, CSR slave @ base 0x000)
//     -> hyperram_avalon (Avalon-MM slave -> ctrl -> SDR PHY)
//   (the two are wired here directly — the pure-structural rtl/bench/hyperram_bw_top.sv inlined —
//    so the av_* Avalon handshake is visible to the hyperbus_capture DEBUG module, CSR @ base 0x100)
//         |  split HyperBus device pins (hb_*_o / hb_*_oe / hb_*_i)
//         v
//   hyperbus_pads_altera (fpga/axc3000/)  = inferred tri-state I/O -> real inout board pads
//
// The measured WR_CYCLES/RD_CYCLES cover only the on-chip Avalon datapath; JTAG is control plane
// only (PLAN §8 method E compliant). Read back with sysconsole/bw_read.tcl.
//
// CLOCK PLAN (DDIO PHY, CK_SCHEME="FABRIC2X" — issue #8; ONE clock in the I/O periphery so the
// Fitter 24403/24404 two-phase blocker never applies): the frozen controller is WORD-per-clk and the
// Agilex-3 DDIO PHY (rtl/phy/hyperbus_phy_altera.sv) moves 2 bytes per clk on the two CK edges.
// clk = CK_MHZ drives the controller, the fabric, and every I/O-cell DDIO (DQ/RWDS out). hb_ck is
// generated by the PHY's FABRIC2X scheme — the silicon-proven SDR-PHY CK generator on clk2x
// (= 2x CK, 0 deg, CORE-ONLY: it clocks no I/O cell) — rising/falling edges land at T/4 / 3T/4,
// centred in both byte eyes by construction, no pin delay chain needed.
//
// Board signal names below match quartus/constraints/axc3000_board.tcl (sourced via pins.tcl).

`timescale 1ns/1ps

module top (
    // ---- board clock / reset ----
    input  wire        CLK_25M_C,   // 25 MHz fixed XO (PIN_A7, 1.2 V)
    input  wire        USER_BTN,    // S2, active-low, weak pull-up (PIN_A12, 1.2 V)

    // ---- HyperRAM (Winbond W957D8NB, single-ended x8 HyperBus, 1.2 V) ----
    inout  wire [7:0]  hb_dq,       // DQ[7:0]
    inout  wire        hb_rwds,     // RWDS
    output wire        hb_cs_n,     // chip select
    output wire        hb_ck,       // HyperBus clock (single-ended: no hb_ck_n board pin)
    output wire        hb_rst_n,    // device reset

    // ---- user LEDs (active-low, 3.3-V LVCMOS) — quick visual STATUS ----
    output wire        LED1,        // STATUS.done  (lit = done)
    output wire        RLED,        // STATUS.error (lit = error)
    output wire        GLED         // PLL locked   (lit = locked)
);

  // =========================================================================
  // Qsys backbone: clocks, reset, JTAG-Avalon master
  // =========================================================================
  wire        clk;          // CK_MHZ HyperBus CK word clock (controller + fabric + ALL I/O DDIOs)
  wire        clk2x;        // same-frequency fabric clock (0 deg) — debug capture only (legacy name)
  wire        pll_locked;
  wire        sys_rst;      // synchronous, active-high fabric reset (clk domain)

  // Exported (byte-addressed) Avalon-MM master from the JTAG bridge
  wire [31:0] m_address;
  wire [31:0] m_readdata;
  wire        m_read;
  wire        m_write;
  wire [31:0] m_writedata;
  wire        m_waitrequest;
  wire        m_readdatavalid;
  wire [3:0]  m_byteenable;

  bw_sys u_sys (
    .clk_25_clk           (CLK_25M_C),
    .reset_reset          (~USER_BTN),      // button pressed (low) => assert active-high reset
    .clk_clk              (clk),
    .clk90_clk            (clk2x),          // Qsys export "clk90" now = 100 MHz 2x byte clock (0 deg)
    .locked_export        (pll_locked),
    .sys_reset_reset      (sys_rst),
    .master_address       (m_address),
    .master_readdata      (m_readdata),
    .master_read          (m_read),
    .master_write         (m_write),
    .master_writedata     (m_writedata),
    .master_waitrequest   (m_waitrequest),
    .master_readdatavalid (m_readdatavalid),
    .master_byteenable    (m_byteenable)
  );

  // =========================================================================
  // Byte-address (JTAG master)  ->  word-address CSR slave adapter + 2-slave address decode
  //   Two CSR slaves hang off the single JTAG-Avalon master, decoded on byte-address bit [8]:
  //     m_address[8] == 0  ->  hyperram_bw_test CSR   (base 0x000, regs 0x00..0x7C)
  //     m_address[8] == 1  ->  hyperbus_capture CSR   (base 0x100, regs 0x100..0x110)
  //   Both slaves are word-addressed (csr_address = byte_offset>>2), read combinationally, and tie
  //   waitrequest low. The JTAG-Avalon master is byte-addressed, pipelined (expects readdatavalid),
  //   and single-outstanding, so a combinational readdata/waitrequest mux on the decode bit is safe.
  // =========================================================================
  // issue #13: widened 4->5 for the new REG_DBG/EMAP/PAT/WRAP registers (words 14..20). 0x7C < 0x100
  // so sel_cap = m_address[8] is unaffected by the extra CSR bit.
  localparam int CSR_AW = 5;   // 32 bw_test regs (0x00..0x7C): STATUS/LEN/BASE/cycles/ERR + first-err diag + issue-#13 knobs

  wire              sel_cap        = m_address[8];    // 0x100 window = capture CSR

  // hyperram_bw_test CSR: 32 registers (0x00..0x7C) => 5 word-address bits = m_address[6:2]
  wire [CSR_AW-1:0] csr_address    = m_address[CSR_AW+1:2];
  wire              csr_read       = m_read  & ~sel_cap;
  wire              csr_write      = m_write & ~sel_cap;
  wire [31:0]       csr_writedata  = m_writedata;
  wire [31:0]       csr_readdata;
  wire              csr_waitrequest;

  // hyperbus_capture CSR: 5 registers (0x100..0x110) => 3 word-address bits = m_address[4:2]
  // (issue #13: widened 2->3 for REG_CAPCFG at word 4 / byte 0x110 = the Nth-CS#-fall trigger)
  wire [2:0]        cap_address    = m_address[4:2];
  wire              cap_read       = m_read  & sel_cap;
  wire              cap_write      = m_write & sel_cap;
  wire [31:0]       cap_readdata;
  wire              cap_waitrequest;

  wire [31:0] mux_readdata    = sel_cap ? cap_readdata    : csr_readdata;
  wire        mux_waitrequest = sel_cap ? cap_waitrequest : csr_waitrequest;

  assign m_waitrequest = mux_waitrequest;   // tied low inside both slaves (0 wait states)

  // Pipeline the combinational CSR read to the Avalon read-latency-1 contract the JTAG master expects:
  // capture readdata on the accept cycle, present it with readdatavalid the next cycle.
  logic [31:0] rd_hold;
  logic        rdv_q;
  always_ff @(posedge clk) begin
    if (sys_rst) begin
      rd_hold <= 32'd0;
      rdv_q   <= 1'b0;
    end else begin
      rd_hold <= mux_readdata;                       // both slaves read combinationally off the address
      rdv_q   <= m_read & ~mux_waitrequest;          // accepted this cycle (waitrequest is 0)
    end
  end
  assign m_readdata      = rd_hold;
  assign m_readdatavalid = rdv_q;

  // =========================================================================
  // LED status snoop — latch STATUS bits whenever the host polls STATUS (read of word 0).
  //   STATUS: bit0 = busy, bit1 = done, bit2 = error  (see docs/BW_TEST.md)
  //   bw_test exposes STATUS only via the CSR, so we passively observe the read bus (no 2nd master).
  // =========================================================================
  logic led_done_q, led_error_q;
  always_ff @(posedge clk) begin
    if (sys_rst) begin
      led_done_q  <= 1'b0;
      led_error_q <= 1'b0;
    end else if (csr_read && (csr_address == CSR_AW'(0))) begin
      led_done_q  <= csr_readdata[1];
      led_error_q <= csr_readdata[2];
    end
  end

  // Heartbeat blink so a freshly-flashed bitstream is visually obvious on the board (this build:
  // GLED BLINKS ~1.5 Hz while the PLL is locked, instead of the previous steady-on). Bump the toggle
  // bit / LED on each new build to tell successive flashes apart.
  logic [25:0] hb_cnt;
  always_ff @(posedge clk) begin
    if (sys_rst) hb_cnt <= '0;
    else         hb_cnt <= hb_cnt + 1'b1;
  end
  wire heartbeat = hb_cnt[25];   // SPEED build: ~1.5 Hz at 100 MHz clk (blink rate scales with CK => visible speed marker)

  assign LED1 = ~led_done_q;                 // active-low: lit when done
  assign RLED = ~led_error_q;                // active-low: lit when error
  assign GLED = ~(pll_locked & heartbeat);   // active-low: BLINKS ~1.5 Hz while PLL locked (new build marker)

  // =========================================================================
  // Bandwidth harness: bw_test (CSR + Avalon master) -> hyperram_avalon (Agilex PHY) -> split pins
  // =========================================================================
  // Split HyperBus device pins between the IP and the board pad ring. (The old hb_*_int capture
  // probe stand-ins were retired here: issue #13 re-points the capture probes at the fabric-side
  // phy_*_w controller nets — see the cap_* wires just above the u_cap instance below.)
  wire        init_done;

  // ---- bench master <-> HyperBus IP slave Avalon-MM link, hoisted to the top --------------------
  // DEBUG TAP: this inlines the pure-structural rtl/bench/hyperram_bw_top.sv (identical parameters
  // and wiring — the core modules are NOT modified) so the hyperbus_capture debug module below can
  // observe the av_* handshake alongside the HyperBus pins.
  wire [31:0] av_address;
  wire [15:0] av_burstcount;
  wire        av_read;
  wire        av_write;
  wire [15:0] av_writedata;
  wire [15:0] av_readdata;
  wire        av_readdatavalid;
  wire        av_waitrequest;
  wire [31:0] av_dbg;         // DEBUG: ctrl/front-end/FIFO taps (see hyperram_avalon dbg_bus map)

  // ---- issue #13 live debug bundle: bench (u_bw) outputs -> ctrl / gpio / front-end ------------
  // Unlike the cal_* outputs (readback-only, dangling — the I/O-layer knobs are QSF-static), these
  // are WIRED to the fabric (amendment A8): the host pokes REG_DBG/REG_WRAP at runtime and the knob
  // acts this build. All live in the single `clk` domain shared by bench/ctrl/gpio -> no CDC.
  wire [3:0]  dbg_wr_lat_trim;
  wire [3:0]  dbg_lat_clocks;
  wire        dbg_cr0_reprog;
  wire        dbg_prewin_drive;
  wire [2:0]  dbg_prewin_n;
  wire        dbg_prewin_marker;
  wire        dbg_postwin_hold;
  wire        dbg_ck_stretch_off;   // -> u_io: disable the board ck_stretch trailing masked cycle
  wire        dbg_prewin_contig;    // -> u_ctrl: round 2 (A) keep shadow at a contiguous command-edge reopen
  wire        dbg_end_cwrite;        // -> u_ctrl: round 3 (B) end-of-row (BURST_BOUNDARY-aligned) commit-WRITE
  wire        dbg_spray_defuse;      // -> u_ctrl: round 4 per-boundary spray defuse (REG_DBG[18])
  wire        wrap_en;              // -> u_fe: drive front-end cmd_wrap for the wrapped-write probe

  hyperram_bw_test #(
    .DATA_WIDTH     (16),
    .ADDR_WIDTH     (32),
    .LEN_WIDTH      (16),
    .BURST_WORDS    (64),              // EXPERIMENT: larger Avalon burst — writes LEN<=64 as ONE HyperBus
                                       // burst (no write->write boundary) to confirm the boundary drop
    .CSR_ADDR_WIDTH (CSR_AW),
    .VERSION_MAGIC  (32'h4842_5755),   // "HBWU" — issue #13 instrumented build (was 0x48425754 "HBWT")
    // REG_CAL (word 13/0x34) POR seed: [3:1]=preamble_skip=1 matches u_io's RD_PREAMBLE_SKIP(1).
    // On THIS build the cal_* outputs are readback-only: the GPIO-cell I/O layer's knobs are
    // compile-time (ALTERA DDIO runtime paths are issue-#3-gated), so they fan out nowhere.
    .CAL_RESET      (32'h0000_0002),
    // REG_DBG (word 14/0x38) POR seed (amendment A2): [7:4]=dbg_lat_clocks=6, [3:0]=dbg_wr_lat_trim=3
    // -> bit-identical to this instance's ctrl LATENCY_CLOCKS(6) / WR_LAT_TRIM(3) below, so out of
    // reset the live seeds equal the parameters (POR-legacy). All other REG_DBG bits reset 0.
    .DBG_RESET      (32'h0000_0063)
  ) u_bw (
    .clk             (clk),     // 50 MHz CK word clock (controller)
    .rst             (sys_rst),
    // CSR slave (word addressed) — driven by the JTAG-Avalon master through the adapter above
    .csr_address     (csr_address),
    .csr_read        (csr_read),
    .csr_readdata    (csr_readdata),
    .csr_write       (csr_write),
    .csr_writedata   (csr_writedata),
    .csr_waitrequest (csr_waitrequest),
    // Avalon-MM master -> hyperram_avalon slave (tapped by hyperbus_capture)
    .m_address       (av_address),
    .m_burstcount    (av_burstcount),
    .m_read          (av_read),
    .m_write         (av_write),
    .m_writedata     (av_writedata),
    .m_readdata      (av_readdata),
    .m_readdatavalid (av_readdatavalid),
    .m_waitrequest   (av_waitrequest),
    // Runtime read-eye cal outputs (v9): readback-only here — see the CAL_RESET note above.
    .cal_capture_phase (),
    .cal_preamble_skip (),
    .cal_rx_tap        (),
    .cal_pair_skew     (),
    // issue #13 live debug bundle (spec §0). WIRED, not dangling (A8) — these reach ctrl/gpio/fe.
    .dbg_wr_lat_trim   (dbg_wr_lat_trim),
    .dbg_lat_clocks    (dbg_lat_clocks),
    .dbg_cr0_reprog    (dbg_cr0_reprog),
    .dbg_prewin_drive  (dbg_prewin_drive),
    .dbg_prewin_n      (dbg_prewin_n),
    .dbg_prewin_marker (dbg_prewin_marker),
    .dbg_postwin_hold  (dbg_postwin_hold),
    .dbg_ck_stretch_off(dbg_ck_stretch_off),
    .dbg_prewin_contig (dbg_prewin_contig),
    .dbg_end_cwrite     (dbg_end_cwrite),
    .dbg_spray_defuse   (dbg_spray_defuse),
    .wrap_en           (wrap_en)
  );

  // ---- native fe <-> ctrl channels (hyperram_avalon inlined for the GPIO-cell I/O build) ------
  wire        cmd_valid, cmd_ready, cmd_read, cmd_reg, cmd_wrap;
  wire [31:0] cmd_addr;
  wire [15:0] cmd_len;
  wire        wr_valid, wr_ready, wr_last;
  wire [15:0] wr_data;
  wire [1:0]  wr_strb;
  wire        rd_valid, rd_ready, rd_last;
  wire [15:0] rd_data;
  wire [1:0]  fe_dbg_state;
  wire [3:0]  ctrl_dbg_state;
  wire [5:0]  ctrl_dbg_rem, ctrl_dbg_seg;

  // ---- ctrl <-> board GPIO-cell I/O layer ----
  wire        phy_cs_n_w, phy_rst_n_w, phy_ck_en_w, phy_dq_oe_w, phy_rwds_oe_w, phy_rd_arm_w;
  wire [15:0] phy_dq_o_w, phy_dq_i_w;
  wire [1:0]  phy_rwds_o_w;
  wire        phy_dq_i_valid_w, phy_rwds_i_w;

  hyperbus_avalon #(
    .DQ_WIDTH   (8),
    .DATA_WIDTH (16),
    .ADDR_WIDTH (32),
    .LEN_WIDTH  (16)
  ) u_fe (
    .clk               (clk),
    .rst               (sys_rst),
    .wrap_en           (wrap_en),   // issue #13: 1 during the REG_WRAP probe -> cmd_wrap=1 (else 0 = linear)
    .avs_address       (av_address),
    .avs_read          (av_read),
    .avs_write         (av_write),
    .avs_writedata     (av_writedata),
    .avs_byteenable    (2'b11),
    .avs_burstcount    (av_burstcount),
    .avs_readdata      (av_readdata),
    .avs_readdatavalid (av_readdatavalid),
    .avs_waitrequest   (av_waitrequest),
    .cmd_valid         (cmd_valid),
    .cmd_ready         (cmd_ready),
    .cmd_read          (cmd_read),
    .cmd_reg           (cmd_reg),
    .cmd_wrap          (cmd_wrap),
    .cmd_addr          (cmd_addr),
    .cmd_len           (cmd_len),
    .wr_valid          (wr_valid),
    .wr_ready          (wr_ready),
    .wr_data           (wr_data),
    .wr_strb           (wr_strb),
    .wr_last           (wr_last),
    .rd_valid          (rd_valid),
    .rd_ready          (rd_ready),
    .rd_data           (rd_data),
    .rd_last           (rd_last),
    .dbg_state         (fe_dbg_state)
  );

  hyperbus_ctrl #(
    .DQ_WIDTH          (8),
    .DATA_WIDTH        (16),
    .ADDR_WIDTH        (32),
    .LEN_WIDTH         (16),
    .LATENCY_CLOCKS    (6),
    .FIXED_LATENCY     (1'b1),
    .MAX_BURST_WORDS   (1024),        // = one device ROW (2026-07-09 v4). The ramp's ">=1536 chaos"
                                      //   decoded as ROW WRAP (word0 held gen(1024): linear bursts
                                      //   wrap at the 1024-word row, they never cross it), and the
                                      //   "1024 garbles its tail" is the end-at-row law, not tCSM.
                                      //   Row-aligned full-row segments are the optimum: exactly 4
                                      //   words lost per row transition (wound == end-garble zone).
    .PROGRAM_CR        (1'b1),
    .POR_DELAY_CYCLES  (0),
    .INIT_CR0          (16'h8F1F),
    // Silicon findings, now first-class controller features (wave-1 ctrl-cluster):
    .BURST_BOUNDARY_WORDS (16'h0400),  // = the 1024-word device ROW (v4). The old 16 KB (0x2000)
                                       //   finding was this row law at coarser granularity: reads
                                       //   release the bus at a row crossing, writes WRAP to the
                                       //   row start (aliasing corruption). Never cross a row.
    .WR_COMMIT_READ       (1'b0),      // 2026-07-10 silicon: read-shaped interposes NEVER commit a
                                       //   write->write pending tail (FULL_BURST included) — replay
                                       //   below is the working fix, and dropping the interpose also
                                       //   drops its whole-segment re-read cost at every split
    .WR_COALESCE          (1'b1),      // issue #1 direction 4: merge contiguous writes into one CS#
    .WR_COALESCE_WAIT     (8),
    .WR_CHOP_REPLAY       (1'b0),      // E-D VERDICT (2026-07-09): mask-lead does NOT suppress the
                                       //   below-base wound (errors moved to [B-4,B)=[500..503],
                                       //   mask or no mask). ANY write CS# opening adjacent-above
                                       //   committed data destroys the 4 words below its CA base —
                                       //   unsuppressible (rollback/mask/pause/CK all falsified),
                                       //   reads safe, tails commit fine. Replay machinery parked
                                       //   default-off; the endgame is chop AVOIDANCE (coalesce +
                                       //   the largest clean single-CS# budget) + documentation.
    .WR_CHOP_PAUSE_CYCLES (0),         // OFF (v5). The interim "refresh slot" theory was a row-law
                                       //   misattribution: the 804-error signature was ROW-WRAP
                                       //   aliasing, and 512-budget builds passed W2 at pure wound
                                       //   arithmetic with pause=0 too. The dwell only costs read
                                       //   throughput (final-close delay lands in the read window).
    .WR_CHOP_PAUSE_CK     (1'b0),
    .WR_LAT_TRIM          (3)          // silicon-measured: device write window opens 3 CK early
